//C Program to Find the Size of int, float, double and char

#include<stdio.h>
int main()
{
	printf("\nThe size of int datatype:\t%d",sizeof(int));
	printf("\nThe size of float datatype:\t%d",sizeof(float));
	printf("\nThe size of double datatype:\t%d",sizeof(double));
	printf("\nThe size of char datatype:\t%d",sizeof(char));
	return 0;
}
